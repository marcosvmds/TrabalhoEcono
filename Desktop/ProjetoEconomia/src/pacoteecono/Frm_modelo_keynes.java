    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double g = Double.parseDouble(txt_G.getText());  
         double y = Double.parseDouble(txt_Y.getText());  
         double I = Double.parseDouble(txt_I.getText());  
         double X = Double.parseDouble(txt_X.getText()); 
         double M = Double.parseDouble(txt_M.getText()); 
         double co = Double.parseDouble(txt_Co.getText()); 
         double czin = Double.parseDouble(txt_czin.getText()); 
         double cy=0;
         double resul = 0;
         
         //Y = C + I + G + X - M
         //C = Co + c*Y
         
         resul = (co   + I + g + X - M) / (1-czin);
         
         lbl_saida.setText("A renda de equilibrio: " + resul );
    }//GEN-LAST:event_jButton3ActionPerformed

