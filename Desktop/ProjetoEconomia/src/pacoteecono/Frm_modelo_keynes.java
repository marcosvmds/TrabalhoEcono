    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         double g = Double.parseDouble(txt_G.getText());  
         double y = Double.parseDouble(txt_Y.getText());  
         double I = Double.parseDouble(txt_I.getText());  
         double X = Double.parseDouble(txt_X.getText()); 
         double M = Double.parseDouble(txt_M.getText()); 
         double co = Double.parseDouble(txt_Co.getText()); 
         double cy=0;
         double resul = 0;
         
         cy = (y - (co + g + I + X - M ))/y;
         
         resul = 1-cy;
         
         lbl_saida.setText("A propensão marginal a poupar é : " + resul );
         
        
    }//GEN-LAST:event_jButton1ActionPerformed

