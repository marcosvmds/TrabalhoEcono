    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        float qtde_inicial;
        qtde_inicial = Float.parseFloat(jTextField1.getText());
        float qtde_final;
        qtde_final = Float.parseFloat(jTextField2.getText());
        float renda_inicial;
        renda_inicial = Float.parseFloat(jTextField3.getText());
        float renda_final;
        renda_final = Float.parseFloat(jTextField4.getText());
        
        float delta_qtde = (qtde_final - qtde_inicial) / qtde_inicial;
        float delta_renda = (renda_final - renda_inicial) / renda_inicial;
        
        String elasticidade_renda;
        elasticidade_renda = String.format ("%.2f", Math.abs(delta_qtde / delta_renda));
        
        jTextField5.setText(elasticidade_renda);
    }//GEN-LAST:event_jButton1ActionPerformed

